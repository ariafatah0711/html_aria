// perulangan counted

// for
for (let i = 0; i < 10; i++) {
  console.log("perulangan ke-" + i);
}
// cara kerjanya adalah for lalu ada let i = 0 yang artinya membuat var i = 0
// lalu ; i < 10 ini adalah kondisi jika i kurang dari 10 maka akan menjalankan,
// ; i++ yang artinya variabel i ditambah 1 setiap diulang sampai kondisi menjadi i < 10 yaitu saat var i menjadi 10

for (let i = 10; i > 0; i--) {
  console.log("perulangan ke-" + i);
}

// perulangan while
var i = 0;
while (i < 10) {
  console.log("perulagan ke-" + i);
  i++;
}
